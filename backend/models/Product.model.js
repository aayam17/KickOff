const mongoose = require("mongoose");

const productSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true
    },
    team: {
      type: String
    },
    price: {
      type: Number,
      required: true,
      min: 0
    },
    size: {
      type: [String]
    },
    stock: {
      type: Number,
      default: 0,
      min: 0
    },
    image: {
      type: String
    }
  },
  { timestamps: true }
);

module.exports = mongoose.model("Product", productSchema);
